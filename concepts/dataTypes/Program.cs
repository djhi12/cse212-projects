using System;

namespace dataTypes
{
    public class Program
    {
        static void Main(string[] args)
        {
            // Integral types
            byte a = 10;
            int b = -20;

            // Floating-point types
            float c = 3.14f;
            double d = 1.618;

            // Decimal type
            decimal e = 123.456m;

            // Character type
            char f = 'A';

            // Boolean type
            bool g = true;

            // String type
            string h = "Hello, world!";

            Console.WriteLine($"a: {a}, b: {b}, c: {c}, d: {d}, e: {e}, f: {f}, g: {g}, h: {h}");

        }
    }
}