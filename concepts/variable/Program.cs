using System;

namespace Variable
{

    public class Program
    {
        static void Main(string[] args)
        {
            int myNumber = 10; // Declaring and initializing an integer variable
            double myDouble = 3.14; // Declaring and initializing a double variable
            string myString = "Hello, world!"; // Declaring and initializing a string variable

            Console.WriteLine(myNumber); // Output: 10
            Console.WriteLine(myDouble); // Output: 3.14
            Console.WriteLine(myString); // Output: Hello, world!

        }
    }
}