using System;

public class Program
{
    static void Main(string[] args)
    {
        // Sample
        Console.WriteLine("\nRun successfully!!!\n");

    }

    void MultipleLoops(int n)
    {
        for (int i = 0; i < n; i++)
        {
            Console.WriteLine(i);
        }

        for (int j = 0; j < n; j++)
        {
            Console.WriteLine(j);
        }
    }
}
